// The `main` function is the entry point of the program.
// This function is similar to C's main function.
fn main() {
    // Use the `println!` macro to write the string "Hello" to the console.
    println!("Hello");
}
/*
Rust functions implicitly return a value.
By convention, an exit status code of 0 indicates successful program execution.
There's no need for an explicit return statement.
*/
